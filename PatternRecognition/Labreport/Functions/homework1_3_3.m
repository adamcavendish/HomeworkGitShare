N = 500;
m = [0 0];
S = zeros(2,2,8);
DataSet = zeros(N, 2, 8);

S(:,:,1) = [1.0  0.0;  0.0 1.0];
S(:,:,2) = [0.2  0.0;  0.0 0.2];
S(:,:,3) = [2.0  0.0;  0.0 2.0];
S(:,:,4) = [0.2  0.0;  0.0 2.0];
S(:,:,5) = [2.0  0.0;  0.0 0.2];
S(:,:,6) = [1.0  0.5;  0.5 1.0];
S(:,:,7) = [0.3  0.5;  0.5 2.0];
S(:,:,8) = [0.3 -0.5; -0.5 2.0];

cc = jet(8);
figure(1);
hold on;
for i = 1 : 8
    DataSet(:,:,i) = mvnrnd(m, S(:,:,i), N);
    subplot(2, 4, i);
    plot(DataSet(:,1,i), DataSet(:,2,i), '.', 'color', cc(i,:));
    axis equal;
    axis([-7 7 -7 7]);
end

